function cleanTables(db) {
    return db.raw(
        `TRUNCATE
        shopily_shopping_list,
        shopily_item_list,
        shopily_users,
        shopily_supermarkets
        RESTART IDENTITY CASCADE`
    )
}

function seedTables(db, supermarkets, users, item_list, shopping_list) {
    return db
        .into('shopily_supermarkets')
        .insert(supermarkets)
        .then(() => 
            db
                .into('shopily_users')
                .insert(users)
        )
        .then(() => 
            db
                .into('shopily_item_list')
                .insert(item_list)
        )
        .then(() => 
            db
                .into('shopily_shopping_list')
                .insert(shopping_list)
        )
}

